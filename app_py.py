import streamlit as st
import numpy as np
import pickle

# Load model
model = pickle.load(open("/content/clv_model.pkl", "rb"))


st.title("Customer Lifetime Value Prediction")

st.write("Enter customer details to predict CLV")

recency = st.number_input("Recency")
frequency = st.number_input("Frequency")
monetary = st.number_input("Monetary")
tenure = st.number_input("Tenure")

if st.button("Predict"):
    data = np.array([[recency, frequency, monetary, tenure]])
    prediction = model.predict(data)
    st.success(f"Predicted CLV: {prediction[0]:.2f}")